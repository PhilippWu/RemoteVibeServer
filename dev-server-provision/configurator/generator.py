# RVSconfig.yml generation
# ---------------------------------------------------------------------------

# The key order used when writing RVSconfig.yml — keeps the output tidy and
# predictable.
_RVS_KEY_ORDER: list[str] = [
    "domain",
    "subdomain",
    "email",
    "cloudflare_api_token",
    "cloudflare_zone_id",
    "coder_admin_password",
    "enable_agent_copilot",
    "enable_agent_claude",
    "enable_agent_gemini",
    "enable_agent_codex",
    "enable_agent_opencode",
    "openai_api_key",
    "anthropic_api_key",
    "google_api_key",
    "github_token",
    "codex_openai_auth_code",
    "opencode_provider",
]


def _yaml_escape(value: str) -> str:
    r"""Escape a string for safe inclusion in a double-quoted YAML scalar.

    Handles backslashes, double quotes, and newlines so the generated
    ``RVSconfig.yml`` is always valid YAML.
    """
    return value.replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n")


def generate_rvs_config(config: dict[str, Any]) -> str:
    """Return a simple YAML string (``RVSconfig.yml``) from *config*.

    The file is designed to be copy-pasted into ``install.sh`` on a bare
    Ubuntu server.  Boolean flags are written as ``true`` / ``false``.
    Empty values are represented as empty strings (``key: ""``).
    """
    lines: list[str] = [
        "# RVSconfig.yml — RemoteVibeServer configuration",
        "# Generated by the RemoteVibeServer Configurator.",
        "# Paste this into the install.sh prompt on your server.",
        "",
    ]
    for key in _RVS_KEY_ORDER:
        value = config.get(key)
        if isinstance(value, bool):
            lines.append(f"{key}: {str(value).lower()}")
        elif value:
            lines.append(f'{key}: "{_yaml_escape(str(value))}"')
        else:
            lines.append(f"{key}: \"\"")
    lines.append("")  # trailing newline
    return "\n".join(lines)
