class TestLoadConfigFile(unittest.TestCase):

    def test_load_cloud_init(self):
        p = _write_tmp(_CLOUD_INIT_SAMPLE)
        try:
            result = load_config_file(p)
            self.assertEqual(result["domain"], "example.com")
            self.assertIs(result["enable_agent_copilot"], True)
        finally:
            p.unlink(missing_ok=True)

    def test_load_rvs_config(self):
        p = Path(tempfile.mkdtemp()) / "RVSconfig.yml"
        p.write_text(_RVS_CONFIG_SAMPLE, encoding="utf-8")
        try:
            result = load_config_file(p)
            self.assertEqual(result["domain"], "example.com")
        finally:
            p.unlink(missing_ok=True)

    def test_empty_file_returns_empty_dict(self):
        p = _write_tmp("")
        try:
            self.assertEqual(load_config_file(p), {})
        finally:
            p.unlink(missing_ok=True)

    def test_garbage_returns_empty_dict(self):
        p = _write_tmp("this is not a config file\nrandom: stuff\n")
        try:
            self.assertEqual(load_config_file(p), {})
        finally:
            p.unlink(missing_ok=True)


