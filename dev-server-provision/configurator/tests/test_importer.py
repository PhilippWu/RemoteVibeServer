def _write_tmp(content: str, suffix: str = ".yaml") -> Path:
    """Write *content* to a temporary file and return its path."""
    fd, name = tempfile.mkstemp(suffix=suffix)
    try:
        os.write(fd, content.encode("utf-8"))
    finally:
        os.close(fd)
    return Path(name)


