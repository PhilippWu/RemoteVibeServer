class TestFindConfigFiles(unittest.TestCase):

    def test_finds_cloud_init(self):
        with tempfile.TemporaryDirectory() as d:
            p = Path(d) / "cloud-init.yaml"
            p.write_text(_CLOUD_INIT_SAMPLE, encoding="utf-8")
            found = find_config_files(d)
            self.assertIn(p, found)

    def test_finds_rvs_config(self):
        with tempfile.TemporaryDirectory() as d:
            p = Path(d) / "RVSconfig.yml"
            p.write_text(_RVS_CONFIG_SAMPLE, encoding="utf-8")
            found = find_config_files(d)
            self.assertIn(p, found)

    def test_finds_both(self):
        with tempfile.TemporaryDirectory() as d:
            ci = Path(d) / "cloud-init.yaml"
            ci.write_text(_CLOUD_INIT_SAMPLE, encoding="utf-8")
            rv = Path(d) / "RVSconfig.yml"
            rv.write_text(_RVS_CONFIG_SAMPLE, encoding="utf-8")
            found = find_config_files(d)
            self.assertIn(ci, found)
            self.assertIn(rv, found)

    def test_empty_dir_returns_empty(self):
        with tempfile.TemporaryDirectory() as d:
            self.assertEqual(find_config_files(d), [])

    def test_glob_cloud_init_variants(self):
        with tempfile.TemporaryDirectory() as d:
            p = Path(d) / "cloud-init.old.yaml"
            p.write_text(_CLOUD_INIT_SAMPLE, encoding="utf-8")
            found = find_config_files(d)
            self.assertIn(p, found)

    def test_sorted_newest_first(self):
        with tempfile.TemporaryDirectory() as d:
            old = Path(d) / "cloud-init.yaml"
            old.write_text(_CLOUD_INIT_SAMPLE, encoding="utf-8")
            import time
            time.sleep(0.01)
            new = Path(d) / "RVSconfig.yml"
            new.write_text(_RVS_CONFIG_SAMPLE, encoding="utf-8")
            found = find_config_files(d)
            self.assertEqual(found[0], new)


